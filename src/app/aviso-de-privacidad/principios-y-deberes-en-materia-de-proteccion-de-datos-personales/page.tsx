export default function PrincipiosDeberesProteccionDatosPersonales() {
  return (
    <section className="w-full max-w-4xl px-4 my-12 flex flex-col items-center mx-auto">
      <div className="w-full">
        <h2 className="text-2xl md:text-3xl font-bold mb-4 text-center">
          Principios y deberes en materia de protección de datos personales
        </h2>
        <p className="mb-4 text-justify">
          Los siguientes son los principios y deberes que debemos cumplir en CAIINNO, en materia de protección de datos personales, es importante que los leas y conozcas:
        </p>
        <ul className="list-disc pl-6 mb-4 text-justify">
          <li>
            <strong>Licitud:</strong> obliga al responsable al tratamiento de datos personales con apego a lo dispuesto por la normativa aplicable;
          </li>
          <li>
            <strong>Lealtad:</strong> obliga al responsable a tratar los datos personales privilegiando la protección de los intereses del titular y la expectativa razonable de privacidad, y sin el uso de medios engañosos o fraudulentos para recabar y tratar los datos personales;
          </li>
          <li>
            <strong>Consentimiento:</strong> obliga al responsable a recabar el consentimiento del titular para el tratamiento de sus datos, salvo las excepciones previstas por la ley;
          </li>
          <li>
            <strong>Información:</strong> obliga al responsable a informar al titular sobre la existencia y características del tratamiento que realiza, a través del aviso de privacidad, leyenda de información o documento similar, según corresponda;
          </li>
          <li>
            <strong>Proporcionalidad:</strong> obliga al responsable a tratar únicamente los datos personales que sean necesarios, adecuados y relevantes en relación con las finalidades para las que se hayan obtenido;
          </li>
          <li>
            <strong>Finalidad:</strong> obliga al responsable a tratar los datos personales para las finalidades (claras y determinadas) para las cuales se obtuvieron los datos personales y fueron informadas al titular, y consentidas por este último, en su caso;
          </li>
          <li>
            <strong>Calidad:</strong> obliga al responsable a tomar las medidas necesarias para procurar que los datos personales tratados sean exactos, completos y actualizados, y que se eliminen una vez que concluyó el tratamiento, y
          </li>
          <li>
            <strong>Responsabilidad:</strong> obliga al responsable a responder y rendir cuentas por los datos personales que trata él mismo o los encargados que los tratan a su nombre y por su cuenta.
          </li>
        </ul>
        <p className="mb-2 text-justify font-bold">Deberes de:</p>
        <ul className="list-disc pl-6 mb-4 text-justify">
          <li>
            <strong>Confidencialidad:</strong> obliga al responsable a no comunicar ni divulgar los datos personales que trata, y
          </li>
          <li>
            <strong>Seguridad:</strong> obliga al responsable a adoptar medidas de seguridad física, técnicas y administrativas que permitan proteger los datos personales que tratan contra daño, pérdida, alteración, destrucción y el uso, acceso o tratamiento no autorizado.
          </li>
        </ul>
      </div>
    </section>
  );
}